    <h1>Introduction</h1>
    <p>
        Iban is the language of about a third of the population of Sarawak;
        302,984 people in a total of 957,918 at the census taken in 1970. It
        is also spoken Kalimantan by Iban who live on the north bank of the
        Kapuas River and, in various dialects, by closely related 'Ibanic'
        peoples of the Kapuas and probably further to the south. Within
        Sarawak, Iban is used as a lingua franca wherever Iban have settled.
        There are a few in the western tip of the country and none in the area
        left almost blank in the east, south of the State of Brunei. For the
        most part, Iban longhouses and their lands lie along river valleys in
        hilly country up to about 1,000 feet above sea level, and avoid the
        swampy coastal strip.
    </p>
